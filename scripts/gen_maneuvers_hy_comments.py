def has_cyrillic(text: str) -> bool:
    return bool(re.search(r"[А-Яа-яЁё]", text))


def main() -> None:
    files = [
        ROOT / "data/test/test2/am/with-image/maneuvers-and-lane-position/questions.json",
        ROOT / "data/test/test2/am/without-image/maneuvers-and-lane-position/questions.json",
    ]
    ids: set[str] = set()
    for f in files:
        for q in json.loads(f.read_text(encoding="utf-8")):
            ids.add(q["id"])

    missing = sorted(ids - set(COMMENTS.keys()), key=lambda x: (int(x.split("-")[0]), int(x.split("-")[1])))
    extra = sorted(set(COMMENTS.keys()) - ids, key=lambda x: (int(x.split("-")[0]), int(x.split("-")[1])))
    if missing:
        raise SystemExit(f"Missing comments for: {missing}")
    if extra:
        raise SystemExit(f"Extra comments not in questions: {extra}")

    for qid, comment in COMMENTS.items():
        if has_cyrillic(comment):
            raise SystemExit(f"Cyrillic in comment {qid}: {comment}")

    existing: dict[str, str] = {}
    if OUT.exists():
        existing = json.loads(OUT.read_text(encoding="utf-8"))

    merged = {**existing, **COMMENTS}
    OUT.write_text(
        json.dumps(merged, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"Written {len(COMMENTS)} maneuver comments to {OUT.name}")
    print(f"Total keys in override file: {len(merged)}")


if __name__ == "__main__":
    main()