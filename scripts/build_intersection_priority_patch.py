def combined_text(item: dict) -> str:
    parts = [item.get("question", ""), item.get("explanation", "")]
    for option in item.get("options", []):
        parts.append(option.get("text", ""))
    return "\n".join(parts)


def extract_sign_ids(text: str) -> list[str]:
    found: list[str] = []
    for match in SIGN_PATTERN.findall(text):
        if match in SIGNS_IDS and match not in found:
            found.append(match)
    if "շրջանաձև" in text and "4.3" not in found:
        found.append("4.3")
    return found


def extract_marking_ids(text: str) -> list[str]:
    found: list[str] = []
    for match in MARKING_PATTERN.findall(text):
        if match in MARKING_IDS and match not in found:
            found.append(match)
    return found


def extract_term_slugs(text: str) -> list[str]:
    lowered = text.casefold()
    found: list[str] = []
    for slug, term_text in TERM_PATTERNS:
        if term_text.casefold() in lowered and slug not in found:
            found.append(slug)
    for slug, keywords in TERM_KEYWORDS.items():
        if slug in found:
            continue
        if any(keyword.casefold() in lowered for keyword in keywords):
            found.append(slug)
    return found


def normalize_entity_refs(item: dict, ru_refs: list[dict] | None) -> list[dict]:
    text = combined_text(item)
    sign_ids = extract_sign_ids(text)
    marking_ids = [mark_id for mark_id in extract_marking_ids(text) if mark_id not in sign_ids]
    term_ids = extract_term_slugs(text)

    if ru_refs:
        for ref in ru_refs:
            ref_type = ref.get("type")
            ids = [value for value in ref.get("ids", []) if value]
            if ref_type == "term":
                for value in ids:
                    if value not in term_ids:
                        term_ids.append(value)

    return [
        {"type": "sign", "ids": sign_ids},
        {"type": "marking", "ids": marking_ids},
        {"type": "term", "ids": term_ids},
    ]


def load_questions() -> list[dict]:
    items: list[dict] = []
    for path in AM_FILES:
        items.extend(json.loads(path.read_text(encoding="utf-8")))
    return items


def load_ru_by_id() -> dict[str, dict]:
    ru_by_id: dict[str, dict] = {}
    for item in json.loads(RU_FILE.read_text(encoding="utf-8")):
        ru_by_id[item["id"]] = item
    return ru_by_id


def main() -> None:
    questions = load_questions()
    ru_by_id = load_ru_by_id()
    patch: dict[str, dict] = {}

    for item in questions:
        qid = item["id"]
        if qid in ENTITY_OVERRIDES:
            entity_refs = ENTITY_OVERRIDES[qid]
        else:
            ru_item = ru_by_id.get(qid)
            entity_refs = normalize_entity_refs(
                item,
                ru_item.get("entityRefs") if ru_item else None,
            )
        comment = COMMENTS.get(qid, "").strip()
        if not comment:
            raise ValueError(f"Missing comment for {qid}")
        patch[qid] = {"comment": comment, "entityRefs": entity_refs}

    OUT_FILE.parent.mkdir(parents=True, exist_ok=True)
    OUT_FILE.write_text(
        json.dumps(patch, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(len(patch), OUT_FILE)


if __name__ == "__main__":
    main()