SIGN_PATTERN = re.compile(r"(?<!\d)(?:[1-8]\.\d+(?:\.\d+){0,2})(?!\d)")
MARKING_PATTERN = re.compile(r"(?<!\d)(?:1\.\d+(?:\.\d+)?|2\.\d+(?:\.\d+)?)(?!\d)")

TERM_PATTERNS: list[tuple[str, str, str]] = []
for term in TERMS:
    slug = term["slug"]
    for field in ("term_ru", "term_hy"):
        text = str(term.get(field, "")).strip()
        if len(text) >= 4:
            TERM_PATTERNS.append((slug, field, text))
TERM_PATTERNS.sort(key=lambda item: len(item[2]), reverse=True)


def normalize_entity_refs(item: dict) -> list[dict]:
    text = combined_text(item)
    sign_ids: list[str] = []
    marking_ids: list[str] = []
    term_ids: list[str] = []

    for match in SIGN_PATTERN.findall(text):
        if match in SIGNS_IDS and match not in sign_ids:
            sign_ids.append(match)
    for match in MARKING_PATTERN.findall(text):
        if match in MARKING_IDS and match not in marking_ids:
            marking_ids.append(match)

    lowered = text.casefold()
    for slug, _field, term_text in TERM_PATTERNS:
        if term_text.casefold() in lowered and slug not in term_ids:
            term_ids.append(slug)

    return [
        {"type": "sign", "ids": sign_ids},
        {"type": "marking", "ids": marking_ids},
        {"type": "term", "ids": term_ids},
    ]


