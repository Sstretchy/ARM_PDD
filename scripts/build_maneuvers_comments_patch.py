def load_questions() -> list[dict]:
    paths = [
        TARGET / "with-image/maneuvers-and-lane-position/questions.json",
        TARGET / "without-image/maneuvers-and-lane-position/questions.json",
    ]
    items: list[dict] = []
    for path in paths:
        items.extend(read_json(path))
    items.sort(key=lambda q: int(q["id"].split("-")[1]))
    return items


def main() -> None:
    items = load_questions()
    patch: dict[str, dict] = {}
    missing: list[str] = []

    for item in items:
        qid = item["id"]
        comment = COMMENTS.get(qid, "").strip()
        if not comment:
            missing.append(qid)
        patch[qid] = {
            "comment": comment,
            "entityRefs": normalize_entity_refs(item),
        }

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    OUT_FILE.write_text(
        json.dumps(patch, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )

    print(f"Questions: {len(items)}")
    print(f"Comments: {len(COMMENTS)}")
    print(f"Patch entries: {len(patch)}")
    if missing:
        print(f"MISSING comments: {missing}")
        raise SystemExit(1)
    print(f"Written: {OUT_FILE}")


if __name__ == "__main__":
    main()